import random


"======================================================================="
"========================= CapteurLumiere =============================="
"======================================================================="

class CapteurLumiere:
    "Simule un capteur de lumiere"
    def __init__(self):
        self.lux = 0
        self.luxMin = 0
        self.luxMax = 100000
    def createRandomLux(self):
        return random.uniform(self.luxMin, self.luxMax)